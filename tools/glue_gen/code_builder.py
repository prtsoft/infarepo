    # ------------------------------------------------------------------

    def _build_sequence_generator(self, node: PipelineNode, t: TransformationDef) -> None:
        g = self.graph
        s = self.script
        upstream = g.primary_upstream(node.instance_name)
        in_df = self._resolve_in_df(node.instance_name, upstream)
        out_df = g.df_var(node.instance_name)
        self._node_output[node.instance_name] = out_df

        next_val_port = next((p for p in t.ports if "NEXTVAL" in p.name.upper()), None)
        col_name = next_val_port.name if next_val_port else "SEQ_ID"

        s.section(f"SEQUENCE GENERATOR: {node.instance_name}")
        s.emit(f"# PC Sequence Generator → monotonically_increasing_id()")
        s.emit(f"# NOTE: IDs are unique per Spark job run but NOT sequential integers.")
        s.emit(f"# If you need true sequential IDs, use row_number() with an ORDER BY.")
        s.emit(f"{out_df} = {in_df}.withColumn(")
        s.emit(f'    "{col_name}",')
        s.emit(f"    F.monotonically_increasing_id(),")
        s.emit(f")")

    # ------------------------------------------------------------------
    # Sorter
    # ------------------------------------------------------------------

    def _build_sorter(self, node: PipelineNode, t: TransformationDef) -> None:
        g = self.graph
        s = self.script
        upstream = g.primary_upstream(node.instance_name)
        in_df = self._resolve_in_df(node.instance_name, upstream)
        out_df = g.df_var(node.instance_name)
        self._node_output[node.instance_name] = out_df

        # Sorter ports: INPUT/OUTPUT ports, sort direction in attributes
        sort_cols: List[str] = []
        for port in t.ports:
            if port.port_type == "INPUT/OUTPUT":
                direction = t.attributes.get(f"Direction for {port.name}", "ASCENDING")
                if direction.upper() == "DESCENDING":
                    sort_cols.append(f'F.col("{port.name}").desc()')
                else:
                    sort_cols.append(f'F.col("{port.name}")')

        s.section(f"SORTER: {node.instance_name}")
        if sort_cols:
            cols_str = ", ".join(sort_cols)
            s.emit(f"# NOTE: In Spark, orderBy has no performance benefit unless writing")
            s.emit(f"# to a sorted file format. Evaluate if this sort is truly needed.")
            s.emit(f"{out_df} = {in_df}.orderBy({cols_str})")
        else:
            s.todo(f"No sort columns detected in {node.instance_name} — add orderBy columns")
            s.emit(f"{out_df} = {in_df}")

    # ------------------------------------------------------------------
    # Normalizer
    # ------------------------------------------------------------------

    def _build_normalizer(self, node: PipelineNode, t: TransformationDef) -> None:
        g = self.graph
        s = self.script
        upstream = g.primary_upstream(node.instance_name)
        in_df = self._resolve_in_df(node.instance_name, upstream)
        out_df = g.df_var(node.instance_name)
        self._node_output[node.instance_name] = out_df

        s.section(f"NORMALIZER: {node.instance_name}")

        # Group OUTPUT ports by their REF_SOURCE_FIELD attribute.
        # Each group represents one "occurrence" column (e.g. item_1, item_2, item_3).
        # Pass-through (non-repeated) ports have an empty REF_SOURCE_FIELD.
        from collections import defaultdict
        groups: dict = defaultdict(list)
        passthrough_cols: list = []

        for port in t.ports:
            if "OUTPUT" not in port.port_type.upper():
                continue
            if port.ref_source_field:
                groups[port.ref_source_field].append(port.name)
            else:
                passthrough_cols.append(port.name)

        if not groups:
            # No REF_SOURCE_FIELD metadata — fall back to a generic stub
            s.warn(
                f"NORMALIZER {node.instance_name}: no REF_SOURCE_FIELD metadata found. "
                "Inspect source column structure and implement manually."
            )
            s.emit(f"# Use F.explode(F.array(...)) for repeated column groups")
            s.emit(f"{out_df} = {in_df}  # TODO: implement NORMALIZER logic")
            return

        # Emit F.array([F.struct(...)]) + F.explode for each group
        passthrough_str = (
            "F.col(" + "), F.col(".join(f'"{c}"' for c in passthrough_cols) + ")"
            if passthrough_cols else ""
        )

        s.emit(f"# NORMALIZER: transpose repeated column groups into rows")
        for group_key, cols in groups.items():
            s.emit(f"# Group '{group_key}': columns {cols}")

        if len(groups) == 1:
            group_key, cols = next(iter(groups.items()))
            # One struct per occurrence column — each becomes its own row after explode.
            # E.g. ITEM_1, ITEM_2, ITEM_3 → 3 rows, field named by group_key.
            array_elements = ", ".join(
                f'F.struct(F.col("{c}").alias("{group_key}"))' for c in cols
            )
            s.emit(f"# One struct per occurrence column so explode yields one row per occurrence")
            s.emit(f'{out_df} = {in_df}.select(')
            if passthrough_cols:
                pt_cols = ", ".join(f'F.col("{c}")' for c in passthrough_cols)
                s.emit(f'    {pt_cols},')
            s.emit(f'    F.explode(F.array({array_elements})).alias("_norm"),')
            s.emit(f').select(')
            if passthrough_cols:
                s.emit(f'    {pt_cols},')
            s.emit(f'    F.col("_norm.{group_key}").alias("{group_key}"),')
            s.emit(f')')
        else:
            # Multiple groups — needs a cross-product explode; emit skeleton
            s.warn(
                f"NORMALIZER {node.instance_name} has {len(groups)} groups — "
                "multi-group normalization requires manual wiring."
            )
            for group_key, cols in groups.items():
                s.emit(f'# Group {group_key!r}: {cols}')
            s.emit(f'{out_df} = {in_df}  # TODO: implement multi-group NORMALIZER')

    # ------------------------------------------------------------------
    # Target
    # ------------------------------------------------------------------

    def _build_target(self, node: PipelineNode) -> None:
        g = self.graph
        s = self.script
        upstream = g.primary_upstream(node.instance_name)
        in_df = self._resolve_in_df(node.instance_name, upstream)

        tgt_def = self.folder.targets.get(node.transformation_name)
        db_type = tgt_def.db_type if tgt_def else "Unknown"
        db_name = tgt_def.db_name if tgt_def else ""
        owner = (tgt_def.owner + ".") if (tgt_def and tgt_def.owner) else ""
        tgt_name = node.transformation_name

        # Select only the columns that the mapping writes to this target
        out_fields = g.output_fields(upstream) if upstream else []

        s.section(f"TARGET: {tgt_name}  ({db_type} / {db_name})")
        s.emit(f"logger.info('Writing target: {tgt_name}')")

        if out_fields:
            col_list = ", ".join(f'F.col("{c}")' for c in out_fields)
            s.emit(f"df_output_{_safe_var(tgt_name)} = {in_df}.select({col_list})")
            write_df = f"df_output_{_safe_var(tgt_name)}"
        else:
            write_df = in_df

        if db_type.upper() == "FLAT FILE":
            self._build_flat_file_target(node, tgt_def, tgt_name, write_df)
        elif _is_lakehouse(db_type):
            self._build_lakehouse_target(node, tgt_def, write_df, db_type)
        else:
            self._build_jdbc_target(node, tgt_def, tgt_name, owner, write_df, db_type)

    def _build_flat_file_target(
        self, node: PipelineNode, tgt_def: Optional[TargetDef],
        tgt_name: str, write_df: str,
    ) -> None:
        s = self.script
        delim = (tgt_def.delimiter if tgt_def and tgt_def.delimiter else ",")
        path_arg = _arg_name(f"S3_OUTPUT_PATH_{_safe_var(tgt_name)}")
        s.add_arg(path_arg)
        s.emit(f"# Writing FLAT FILE target: {tgt_name}")
        s.emit(f"({write_df}.write")
        s.emit(f'    .format("csv")')
        s.emit(f'    .option("header", "true")')
        s.emit(f'    .option("sep", "{delim}")')
        s.emit(f'    .mode("overwrite")')
        s.emit(f'    .save(args["{path_arg}"]))')

    def _has_upstream_update_strategy(self, node_name: str) -> bool:
        """Return True if any node upstream of node_name is an Update Strategy transformation."""
        visited: Set[str] = set()
        queue = list(self.graph.all_upstream_names(node_name))
        while queue:
            n = queue.pop()
            if n in visited:
                continue
            visited.add(n)
            nd = self.graph.nodes.get(n)
            if nd and nd.transformation_def and nd.transformation_def.type == TransformationType.UPDATE_STRATEGY:
                return True
            queue.extend(self.graph.all_upstream_names(n))
        return False

    def _build_jdbc_target(
        self, node: PipelineNode, tgt_def: Optional[TargetDef],
        tgt_name: str, owner: str, write_df: str, db_type: str,
    ) -> None:
        s = self.script
        conn_key = (tgt_def.db_name if tgt_def and tgt_def.db_name else tgt_name)
        conn_arg = _arg_name(f"CONN_TGT_{conn_key}")
        s.add_arg(conn_arg)
        table_ref = f"{owner}{tgt_name}"

        has_us = self._has_upstream_update_strategy(node.instance_name)
        if has_us:
            s.warn(
                f"Target '{tgt_name}' has an upstream Update Strategy — simple overwrite will "
                "truncate the table. Use the _insert/_update/_delete DataFrames from the Update "
                "Strategy section and implement a Delta merge or JDBC upsert instead."
            )
            write_mode = '"append"  # WARNING: upstream Update Strategy detected — see above'
        else:
            write_mode = '"overwrite"  # TODO: change to "append" as needed'

        conn_var = f"_conn_tgt_{_safe_var(conn_key)}"
        s.emit(f'{conn_var} = glueContext.extract_jdbc_conf(args["{conn_arg}"])')
        s.emit(f"({write_df}.write")
        s.emit(f'    .format("jdbc")')
        s.emit(f'    .option("url", {conn_var}["url"])')
        s.emit(f'    .option("dbtable", "{table_ref}")')
        s.emit(f'    .option("user", {conn_var}["user"])')
        s.emit(f'    .option("password", {conn_var}["password"])')
        s.emit(f'    .mode({write_mode})')
        s.emit(f"    .save()")
        s.emit(f")")

    def _build_lakehouse_target(
        self, node: PipelineNode, tgt_def: Optional[TargetDef],
        write_df: str, db_type: str,
    ) -> None:
        s = self.script
        s.add_arg("S3_OUTPUT_PATH")
        db_type_lower = db_type.lower()

        if "delta" in db_type_lower:
            s.emit(f"# Writing to Delta Lake")
            s.emit(f"({write_df}.write")
            s.emit(f'    .format("delta")')
            s.emit(f'    .mode("overwrite")   # TODO: change to "append" or "merge" as needed')
            s.emit(f'    .save(args["S3_OUTPUT_PATH"]))')
        else:
            fmt = "parquet"
            if "orc" in db_type_lower:
                fmt = "orc"
            s.emit(f"# Writing to S3 as {fmt.upper()}")
            s.emit(f"({write_df}.write")
            s.emit(f'    .format("{fmt}")')
            s.emit(f'    .mode("overwrite")')
            s.emit(f'    .option("compression", "snappy")')
            s.emit(f'    .save(args["S3_OUTPUT_PATH"]))')

    # ------------------------------------------------------------------
    # Dispatch
    # ------------------------------------------------------------------

    def _dispatch_transformation(self, node: PipelineNode, t: TransformationDef) -> None:
        dispatch = {
            TransformationType.EXPRESSION:          self._build_expression,
            TransformationType.FILTER:              self._build_filter,
            TransformationType.AGGREGATOR:          self._build_aggregator,
            TransformationType.JOINER:              self._build_joiner,
            TransformationType.LOOKUP:              self._build_lookup,
            TransformationType.ROUTER:              self._build_router,
            TransformationType.UPDATE_STRATEGY:     self._build_update_strategy,
            TransformationType.SEQUENCE_GENERATOR:  self._build_sequence_generator,
            TransformationType.SORTER:              self._build_sorter,
            TransformationType.NORMALIZER:          self._build_normalizer,
        }
        handler = dispatch.get(t.type)
        if handler:
            handler(node, t)
        else:
            g = self.graph
            upstream = g.primary_upstream(node.instance_name)
            in_df = self._resolve_in_df(node.instance_name, upstream)
            out_df = g.df_var(node.instance_name)
            self._node_output[node.instance_name] = out_df
            self.script.todo(
                f"Unsupported transformation type '{t.type.value}' on '{node.instance_name}' "
                "— translate manually"
            )
            self.script.emit(f"{out_df} = {in_df}  # passthrough — review above TODO")


